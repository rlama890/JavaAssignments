package day12.assignment;

public class Q22 {

	public static void main(String[] args) {
		// Write a program to print the table of given numbers. 
		System.out.println("i j");
		for (int i = 2; i <= 5; i++) {//outer 
			
			for (int j = 1; j <= 5; j++) {//inner
				System.out.println(i + " " + j);
				
			}
			System.out.println("==========");
			
		}

	}

}
